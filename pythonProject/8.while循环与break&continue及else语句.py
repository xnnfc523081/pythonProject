#  2.while循环嵌套

#  设置外循环初始值为 1
a = 1
#  设置while循环，循环次数为2次
while a <= 2:
    #打印外循环次数
    print(f"外循环次数：{a}")
    #设置内循环初始值为1
    b = 1
    #设置内循环次数为3
    while b <= 3:
        #打印内循环次数
        print(f"内循环次数：{b}")
        #内层循环加一为下次循环做准备
        b += 1
    #外层循环加一为下次循环做准备
    a += 1

#  需求:使用while循环输出一个三排六列的座位表

#  第1排第1列     第1排第2列      第1排第3列     第1排第4列       第1排第5列       第1排第6列
#  第2排第1列     第2排第2列      第2排第3列     第1排第4列       第2排第5列       第2排第6列
#  第3排第1列     第3排第2列      第3排第3列     第3排第4列       第3排第5列       第3排第6列

#  设置外循环初始值为1
q = 1
#  设置外循环次数为3次
while q <= 3:
    #  设置内循环初始值为1
    w = 1
    #  设置内循环次数为6次
    while w <= 6:
        #  打印排数与列数，并用制表符占据一个单元格（四个英文空格）来禁止换行   默认end参数为\n，此时改为\t
        print(f"第{q}排第{w}列",end = "\t")
        #  列数自加一为下次内循环做准备
        w += 1
    #  一次外循环后（打印q个排数，六列换行）
    print(end = "\n")
    #  设置外循环次数自加一为下次循环做准备
    q += 1

