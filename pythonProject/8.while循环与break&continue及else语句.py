#  1.break语句     中断循环

#  需求：吃五个苹果吃到第三个不吃了

d = 0
while d <= 4:
    d += 1
    if d == 3:
        print(f"吃了{d}个苹果，不吃了")
        break
    print(f"吃了{d}个苹果")


#  2.continue语句    跳过本次循环的剩余部分（跳过以下代码），继续下一次循环

#  需求：吃到第三个有虫子不吃

d = 0
while d <= 4:
    d += 1
    if d == 3:
        print(f"吃了到第{d}个苹果，有虫子不吃了")
        continue
    print(f"吃了第{d}个苹果")


#  3.else语句

f = 1
while f <= 5:
    print(f"这是第{f}次循环")
    if f == 3:
        break                   #break中断循环且不会执行else语句，如果没有else语句则正常执行else语句
    f += 1
else:
    print("循环正常结束")









