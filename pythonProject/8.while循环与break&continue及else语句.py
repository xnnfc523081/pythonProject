#  循环的定义：在Python中，循环是一种基本的控制流结构，用于重复执行一组语句，直到满足某个条件为止

#  1.while循环（条件循环）    while循环允许在给定代码为真（True）时候重复执行代码

#  含义：在每次循环之前都会进行评估，如果条件为真（True），则执行循环体内的代码，如果为假（False），则退出循环，继续执行while循环外的代码

#  需求:输出五次 hello world！

count = 1                               #定义变量来存储循环次数
while count <= 5:                       #当次数 <= 5 时执行while下的代码
    print("hello,world!")
    count += 1                          #需要对count次数进行自加 2 ，否则count不变则代码陷入死循环

#  需求：计算1——5的和
#  1 + 2 +  3+ 4 + 5
#  3 + 3 + 4 +5
#  6 + 4 + 5
#  10 + 5
#  15

num = 1                           #定义一个变量存储数值
sum = 0                           #定义一个变量存储总和
while num <= 5:                   #当条件成立时执行while下的缩进代码
    sum += num                    #结果sum在原来sum上加了num来计算总和           sum值会随着循环赋值变化
    num += 1                      #num自加 1 来进行循环直到不符合循环结束循环
    print("总和为：", sum)         #打印总和结果

