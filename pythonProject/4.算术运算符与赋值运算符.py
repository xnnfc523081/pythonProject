#  算术运算符
#  1. 常用运算符   +  -  *   /      加减乘除

print(1 + 2)   # 加
print(1 - 2)   # 减
print(1 * 2)   # 乘
print(1 / 2)   # 除


#  2.不常用运算符

#  整除    //       先除法得到结果再在数学数轴基础上向左取整数
print(7 // 3)      # 2.xxx在数轴上向左取为2
print(-7 // 3)     # -2.xxx在数轴上向左取为-3


#  取余数/取模    %      两数相处后的余数部分
print(8 % 3)     # 8 / 3 = 2.xxx      8 - 2 * 3 =2
print(-8 % 3)    # -8 / 3 = -2.xxx     -8 - 3 * (-3) = 1


#  指数幂  **

print(2 ** 4)     #  2*2*2*2


