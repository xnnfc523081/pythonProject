#  赋值运算符

#  1.直接赋值运算符   =

aaa = 10                  #直接赋值给变量
print(aaa)

bbb = aaa                 #将一个变量直接赋值给另一个变量
print(bbb)

result = bbb + aaa        #将运算结果赋值给变量
print(result)

#  2.加法赋值运算符    +=       x + = y 相当于 x + y = x

ccc = 3
ccc += 1
print(ccc)

#  3.减法赋值运算符    -=     同上

ccc = 4
ccc -= 1
print(ccc)

#  4.乘法赋值运算符    *=

ccc = 3
ccc *= 3
print(ccc)

#  5.除法赋值运算符     /=

ccc = 3
ccc /= 3
print(ccc)

#  6.整除赋值运算符    //=

ccc = 7
ccc //= 3
print(ccc)

#  7.取余赋值运算符    %=

ccc = 10
ccc %= 3
print(ccc)

#  8.指数赋值运算符   **=

ccc = 3
ccc **= 3
print(ccc)

'复合赋值运算符只能使用已经存在的变量'





