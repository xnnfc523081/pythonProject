#  break & continue    python中控制循环流程的关键字     必须在循环结构中使用（for/while）且经常与if语句联合   不能脱离循环结构

#  1.break    中断循环

#  吃五个苹果，吃到第三个吃饱了不吃了

#  设置吃的次数
for i in range(1,6):
    #  打印吃的次数
    print(f"吃了{i}个苹果")
    #  设置判断语句如果吃的次数等于3
    if i == 3:
        #  打印结果：吃饱了不吃了
        print("吃饱了不吃了")
        #  设置break语句中断循环体直接退出循环
        break
    print()


#  2.continue    跳过本次循环剩余部分（不执行剩下的代码，执行下一次循环）。继续下一次循环

#  设置吃的次数
for i in range(1,6):
    #  检查是否为第三个苹果
    if i == 3:
        #  打印结果：有虫子不吃了
        print(f"第{i}个有虫子不吃了")
        #  设置continue语句跳过本次循环体
        continue
    print(f"吃了{i}个苹果")


#  3.else语句
for ff in range(1,6):
    print(f"循环了{ff}次")
    if ff == 3:
        break                    #break中断循环，以下的代码不会执行，如果没有break，else语句正常执行
else:
    print("循环正常结束")




#  练习题：使用1 2 3 4 可以组成多少个组成无重复的三位数？具体值为多少？

#定义变量存储总数
count = 0
#  遍历百位的四位数字
for a in range(1,5):
    #遍历十位的四位数字
    for b in range(1,5):
        #遍历个位的四位数字
        for c in range(1,5):
            #设置条件a不等于b不等于c这一条件
            if a != b and a != c and b != c :
                #打印具体三位数
                print(a,b,c)
                #当满足以上条件时候count自加一记录满足条件的个数
                count += 1
print(f"满足条件的个数为:{count}")




#  练习题：打印九九乘法表

#  设置外循环行数为9行
for num in range(1,10):
    #  设置内循环列数等于行数
    for aaa in range(1,num+1):
        #  打印乘法表并且禁止换行用制表符对齐文本
        print(f"{num}×{aaa} = {num * aaa}",end = "\t")
    #一次外循环换行，循环9次为9行
    print()























