# 1.元组的定义:
# 在Python中，元组（tuple）是一种不可变的序列类型，它可以包含多个以逗号分隔的值。
# 元组通常用于存储不同类型的数据集合，它们可以包含任意类型的对象，如整数、字符串、列表等。
# 语法格式：变量名 = (元素1，元素2，元素3.....)
# 当元组内只有一个元素，后面需要加上逗号，不然会被视为这个元素的数据类型本身，而不是一个元组。

# 1.定义一个元组
tua = (1,2,3)
print(tua,type(tua))

# 从左往右 下标从0开始
print(tua[0])

# 从右往左  下标从-1开始
print(tua[-1])

# 切片操作
print(tua[1:3])

# 2.遍历元组
tua = [1,2,3,4,5,6]
for i in tua:
    print(i)

# 3.元组内置函数

'检查方法'
# (1) in 运算符 检查元素是否存在
# 语法格式：元素 in tuple

tua = ("hello","world")
print("jack" in tua)

# (2) not in 运算符 检查元素是否不存在
# 语法格式：元素 not in tuple

tua = (1,2,3,"hello")
print("hello" in tua)

# (3) index(value, start, stop)方法
# 查找元组中某个元素值第一次出现的索引(位置)。如果元素不存在于元组中，则抛出一个 ValueError 异常。
# 语法格式: tuple.index(value, start=0, stop=len(tuple))

# 存在元素
num = [2,3,4,14,5]
print(num.index(3,0,5))

# 不存在元素
#print(num.index(9,0,5))

# (4) count(value)方法
# 统计某个元素在元组中出现的次数。如果元素在元组中不存在，则返回 0 。
# 语法格式：tuple.count(value)
# value ：要统计出现次数的元素。

num = [1,2,3,4,4,11,1,2,3]
print(num.count(1))

# (5) len(object)函数
# 获取容器(如列表、元组、字符串、字典、集合等)中元素的数量，
# 即"长度"或"大小"。获取非容器类型(如整型、浮点型等)的长度则抛出一个 TypeError 异常。
# 语法格式：len(object)
# object ：表示需要获取长度的对象

leng = [1,2,3,4]
print(len(leng))

#——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

#总结:

# in 运算符
# 检查元素是否存在
# 语法格式：元素 in tuple
# 返回值：如果元素存在于元组中返回True，否则返回False

# not in 运算符
# 检查元素是否不存在
# 语法格式：元素 not in tuple
# 返回值：如果元素不存在于元组中返回True，否则返回False

# index(value, start, stop) 方法
# 查找元组中某个元素值第一次出现的索引(位置)
# 语法格式: tuple.index(value, start=0, stop=len(tuple))
# 参数说明：
#   value：要查找的元素值
#   start：查找的起始位置，默认为0
#   stop：查找的结束位置，默认为元组长度
# 返回值：元素第一次出现的索引位置
# 注意：如果元素不存在于元组中，则抛出一个 ValueError 异常

# count(value) 方法
# 统计某个元素在元组中出现的次数
# 语法格式：tuple.count(value)
# 参数说明：
#   value：要统计出现次数的元素
# 返回值：元素在元组中出现的次数
# 注意：如果元素在元组中不存在，则返回 0

# len(object) 内置函数
# 获取容器(如列表、元组、字符串、字典、集合等)中元素的数量，即"长度"或"大小"
# 语法格式：len(object)
# 参数说明：
#   object：表示需要获取长度的对象
# 返回值：对象的元素个数
# 注意：获取非容器类型(如整型、浮点型等)的长度则抛出一个 TypeError 异常
