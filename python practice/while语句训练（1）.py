# 编写一个程序，让用户反复输入数字，但只累加正数，忽略负数和零。当用户输入 999 时程序结束。
#
# 功能要求：
# 使用 while 循环让用户反复输入数字
#
# 只累加正数（大于0的数字）
#
# 当输入负数或零时，显示提示信息但不累加
#
# 当输入 999 时，程序结束并显示最终的正数总和
#
# 不使用 break 或 continue
#
# 程序运行示例：
# text
# 正数累加器程序
# 请输入数字（输入999结束程序）
# 请输入一个数字: 5
# 当前正数总和: 5
# 请输入一个数字: -3
# 输入的是负数或零，不累加
# 请输入一个数字: 2
# 当前正数总和: 7
# 请输入一个数字: 0
# 输入的是负数或零，不累加
# 请输入一个数字: 8
# 当前正数总和: 15
# 请输入一个数字: 999
# 程序结束！正数总和: 15
total = 0
num = float(input("请输入数字:"))
while num != 999:
    if 0 < num:
        total += num
        print(f"当前正数总和：{total}")
        num = float(input("请输入数字:"))
    else:
        print("输入的是负数或零，不累加")
        num = float(input("请输入数字:"))
print(f"程序结束,正数总和：{total}")
